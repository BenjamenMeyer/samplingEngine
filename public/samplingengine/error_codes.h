#ifndef ENGINE_ERROR_CODES_H__
#define ENGINE_ERROR_CODES_H__

#include <stdint.h>

#define ENGINE_MASK_ERROR_VALUE				(0x7000000000000000)

#define ENGINE_IS_ERROR_CODE(e) ((e & ((uint32_t)0x8000000000000000)) == ((uint32_t)0x8000000000000000))
#define ENGINE_GET_ERROR_CODE(e) (e & ((uint32_t)(~ENGINE_MASK_ERROR_VALUE)))
#define ENGINE_MAKE_ERROR_CODE(e) ((int32_t)(((uint32_t)e)|((uint32_t)ENGINE_MASK_ERROR_VALUE)))

#define ENGINE_ERROR_SUCCESS				(0x0000000000000000)
#define ENGINE_ERROR_BAD_PARAMETER			(0x0000000000000001)
#define ENGINE_ERROR_NOT_INITIALIZED 		(0x0000000000000002)
#define ENGINE_ERROR_ALREADY_INITIALIZED	(0x0000000000000003)
#define ENGINE_ERROR_FILTER_MISMATCH		(0x0000000000000004)
#define ENGINE_ERROR_MEMORY_ALLOCATION		(0x0000000000000005)
#define ENGINE_ERROR_FILTER_NOT_PRIMED		(0x0000000000000006)

#endif //ENGINE_ERROR_CODES_H__
